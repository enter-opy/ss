	MSG1 DB 'Enter the count:$'
	STR DB 10,11,'Enter the numbers:$'
	STR1 DB 00H, 0AH, 'Bubble sorted:$'
	STR2 DB 00H, 0AH, '$'
	ARRAY DB 10 DUP(0)
DATA ENDS

CODE SEGMENT

ASSUME CS:CODE,DS:DATA

START: 	MOV AX,DATA
	MOV DS,AX
	LEA DX, MSG1
	MOV AH,09H
	INT 21H
	AND AL,OFH
	MOV CL,AL
	MOV CH,00H
	PUSH CX
	MOV AH, 09H
	LEA DX, STR
	INT 21H
	LEA SI,ARRAY
INPUTS:	MOV AH,01H
	INT 21H
	CMP AL
	JZ INPUTS
	MOV [SI],AL
	INC SI
	DEC CX
	JZ INPUTS
	POP CX
	MOV BX,CX
	PUSH BX
	DEC CX
NEXT SCAN:
	MOV BX,CH
	LEA SI,ARRAY
NEXT COMP:
	MOV AL,[SI]
	MOV DL,[SIH]
	CMP AL,DL
	JZ NOSWAP
	INC SI
	DEC BX
	JNZ NEXT COMP
	DEC CX
	JNZ NEXT SCAN
	MOV AH, 09
	LEA DX,STR1
	INT 21H
	POP BX
	MOV CX, BX
	LEA SI,ARRAY
PRINT:	MOV DL
	MOV AH, 02H
	INT 21H
	INC SI
	DEC CX
	JNZ PRINT
	MOV AH,4CH
	INT 21H
	
	CODE ENDS
	END START
